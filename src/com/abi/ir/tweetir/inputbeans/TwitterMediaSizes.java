public class TwitterMediaSizes {

	private TwitterMediaSize large;
	private TwitterMediaSize small;
	private TwitterMediaSize thumb;
	private TwitterMediaSize medium;

	/**
	 * @return the large
	 */
	public TwitterMediaSize getLarge() {
		return large;
	}

	/**
	 * @param large
	 *            the large to set
	 */
	public void setLarge(TwitterMediaSize large) {
		this.large = large;
	}

	/**
	 * @return the small
	 */
	public TwitterMediaSize getSmall() {
		return small;
	}

	/**
	 * @param small
	 *            the small to set
	 */
	public void setSmall(TwitterMediaSize small) {
		this.small = small;
	}

	/**
	 * @return the thumb
	 */
	public TwitterMediaSize getThumb() {
		return thumb;
	}

	/**
	 * @param thumb
	 *            the thumb to set
	 */
	public void setThumb(TwitterMediaSize thumb) {
		this.thumb = thumb;
	}

	/**
	 * @return the medium
	 */
	public TwitterMediaSize getMedium() {
		return medium;
	}

	/**
	 * @param medium
	 *            the medium to set
	 */
	public void setMedium(TwitterMediaSize medium) {
		this.medium = medium;
	}

}
